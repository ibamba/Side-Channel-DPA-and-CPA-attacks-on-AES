/**
 * Find the max value of the set
 * @param myArray : array to find max value
 * @param size : the size of the array
 * 
*/
inline double maxValue( double myArray [], int size)
{
    int i;
    double maxValue;
    maxValue=myArray[0];

    //find the largest no
    for (i=0;i<size;i++)
    {
    	if(myArray[i]>maxValue)
    	    maxValue=myArray[i];
    }
    return maxValue;
}

/**
 *Find the index of the max value of the set 
 * @param myArray : array to find max value
 * @param size : the size of the array
 * 
*/
inline int ArgMax(double myArray [], int size){
	int argmax = 0;
	double maxValue = myArray[0];

	for (int i=0;i<size;i++)
    {
    	if(myArray[i]>maxValue){
    	    maxValue=myArray[i];
    	    argmax = i;
		}
    }
	return argmax;
}

/**
 * Hamming distance between integers a and b
 * @param a
 * @param b
 * 
*/
inline int HammingDistance(int a,int b){
	int x = a ^ b;
	int setBits = 0;
	while (x > 0) {
		setBits += x & 1;
		x >>= 1;
	}
  	return setBits;
}

/**
 * Hamming weight of an integer x
 * @param x
 * 
*/
inline int hamming_weight(int x){
	int HammingWeight = 0 ;
	while (x > 0)
	{
		HammingWeight += x & 1 ;
		x = x >> 1 ;
	}
	return HammingWeight ;
}


